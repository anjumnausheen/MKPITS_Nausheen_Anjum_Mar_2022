using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Program6_loop
{
    internal class Program
    {
        static void Main(string[] args)
        {
            int i;
            for (i = 10; i >= 1; i--)
            {
                Console.WriteLine(" i = " + i);
            }
            Console.ReadLine();
        }
    }
}
